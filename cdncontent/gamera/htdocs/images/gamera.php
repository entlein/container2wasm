        <h2>About</h2>
        <p>GAMERA is a new magnetohydrodynamic (MHD) simulation tool building and improving upon the high-heritage Lyon-Fedder-Mobarry (LFM) code. GAMERA has been written completely from scratch in modern Fortran and provides a flexible, portable, and exascale-capable MHD code. GAMERA features multiple improvements over LFM including: minimal external library dependence, high degree of optimization, OpenMP parallelism allowing use of heterogeneous architectures, and multiple numerics upgrades. Thus, while preserving all key numerical algorithms underlying the LFM code, GAMERA provides a robust and user-friendly solution for sustainable future.</p>
        <p>In CGS, a global magnetosphere configuration of GAMERA will serve as the backbone of the geospace part of the <a href="./">MAGE</a> model, will incorporate the other magnetospheric components and ultimately couple to the <a href="waccm-x.php">WACCM-X</a> model of the atmosphere-ionosphere system.</p>
        <p class="fw-bolder text-center border-top border-bottom p-2">Contact info: <a href="mailto:gamera-devel@listserv.jhuapl.edu">gamera-devel@listserv.jhuapl.edu</a></p>
