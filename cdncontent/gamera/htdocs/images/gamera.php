      <h2>Algorithms </h2>
      <ul>
        <li>Curvilinear grids
          <ul>
            <li>A key distinguishing feature of the LFM/GAMERA MHD algorithm is its finite-volume formulation allowing arbitrary (including non-orthogonal) hexahedral grids.</li>
            <li>This allows adapting the grid to the geometry of the problem (e.g., packing more grid cells around the magnetopause in magnetospheric calculations) and overall high resolving power with a relatively small total number of grid cells.</li>
            <li>GAMERA improves on the previous LFM algorithm by employing a higher order metrics calculations (12th order Gaussian quadrature.)</li>
          </ul>
        </li>
        <li>Partial interface method</li>
        <ul>
          <li>High (7th or 8th) order spatial interpolation of conserved variables.</li>
          <li>Aggressive Partial Donor Cell (PDM) Limiter and Left/Right state splitting on primitive variables. Clipping and non-clipping options.</li>
          <li>Flux calculation uses the gas-kinetic method. However, implementation is flexible and other flux functions can be used.</li>
        </ul>
        <li>Constrained transport (magnetic flux are defined at cell faces, while electric field is defined at cell edges) keeps magnetic field divergenceless to round-off error.</li>
        <li>Pole axis treatment (Ring Average) for spherical and cylindrical geometries.</li>
        <li>Background magnetic field subtraction is necessary in magnetospheric simulations to remove the dominant planetary dipole from field calculations.</li>
        <li>Conservative and semi-conservative energy equation treatment (total and plasma energy formulations.)</li>
        <li>Boris correction is critical in magnetospheric simulations where Alfv&eacute;n speed becomes prohibitevly large near the planet.</li>
        <li>For magnetospheric applications we have also completely rewritten the ionospheric solver from the LFM code (MIX) and integrated the new Fortran code with GAMERA. The new solver (dubbed <a href="remix.php">reMIX</a>) incorporates the standard LFM ionospheric conductivity model, including ionization by solar irradiance and magnetospheric electron precipitation.</li>
      </ul>
