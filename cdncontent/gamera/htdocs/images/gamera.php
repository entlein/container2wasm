      <h2>History</h2>
    
        <p>LFM code was rewritten by J. Lyon, J. Fedder and C. Mobarry at NRL in the 1980's. Its heritage is traced back to pioneering work on Flux Corrected Transport (FCT) schemes in the same group (J. Boris, D. Book, S. Zalesak, K. Hain). Initial simulations started with very high order spatial interpolation schemes (e.g., 20th order), which was later reduced to 8th order standard today. Over the years, the LFM code or its derivatives have been applied to the terrestrial magnetosphere, planetary magnetospheres (Saturn, Neptune, Uranus, Venus), to inner and outer heliosphere, and regional magnetotail simulations. In 2000's the code was MPI-parallelized which allowed very high resolution simulations reproducing magnetopause boundary instabilities (Kelvin-Helmholtz) and flux-tranfer events (FTEs), ionospheric field-aligned currents with remarkable resemblence to observations, and dipolarization fronts in the magnetotail. Also in 2000's, as part of the Center for Integrated Space Weather Modeling (CISM), a geospace model coupling framework was developed, which allowed implementation of coupling with the Rice Convection Model (<a href="rcm.php">RCM</a>) of the inner magnetosphere and the NCAR <a href="https://www.hao.ucar.edu/modeling/tgcm/tie.php" target="_blank">TIEGCM</a> model of the ionosphere-thermosphere. Finally, in the late 2000's LFM was extended to include the capability to simulate multiple ion fluids. This extension has become known as Multi-Fluid LFM (MFLFM).</p>
  
        
        
        
        
        
